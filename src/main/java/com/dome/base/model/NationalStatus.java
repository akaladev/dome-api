package com.dome.base.model;

public class NationalStatus {
    private long id;
    private String name;
    
    public NationalStatus(){
        
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}